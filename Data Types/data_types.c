#include <stdio.h>
#include <stdbool.h>

int main() {
    /* Pieces of data */

    /* short (16 bit single precision value) */
    /* int (32 bit single precision value) */
    /* long (64 bit single precision value) */
    /* float (32 bit single precision floating point value) */
    /* double (64 bit double precision floating point value */
    /* char (1 bit value) */
    /* bool (1 bit value) */

    short shortValue = 0;
    int integerValue = 1;
    long longValue = 2;

    printf("Short: %d\n", shortValue);
    printf("Int: %d\n", integerValue);
    printf("Long: %ld\n", longValue);

    return 0;
}
